class DataTypes {
  public static void main(String[] args) {
    char letter = 'D';
    String title = "Program Manager";
    int number = 7;
    float decimal = 3.14f;
    boolean result = false;

    System.out.println("First initial is " + letter);
    System.out.println("My job is " + title);
    System.out.println("Favorite number: " + number);
    System.out.println("Love to eat: " + decimal);
    System.out.println("True: " + result);
  }
}
